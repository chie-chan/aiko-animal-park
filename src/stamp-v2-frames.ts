interface BuildArgs {
  themeTitle: string;
  frameInstruction: string;
  paletteBlock: string;
  bgInstruction?: string;
  extraForbidden?: string;
}

function buildFramePrompt(args: BuildArgs, input: PromptInput, g: GridSize): string {
  const n = cellCount(g);
  const bg = args.bgInstruction
    ?? `【背景】
グリッド全体の背景は完全な白（純白）。枠の外には影・装飾・テクスチャを入れない。`;

  return `添付画像のペットを参考に、${g}×${g}グリッド・全${n}種類の${args.themeTitle}LINEスタンプ用イラストを作成してください。

${petBlock(input, g)}

${applyGridSize(args.frameInstruction, g)}

${COMMON_TWO_LAYER}

${applyGridSize(args.paletteBlock, g)}

${commonGrid(g)}

${commonPoses(g)}

${commonTextInstructions(g, input)}

${bg}

${COMMON_FORBIDDEN}
${args.extraForbidden ? "\n" + applyGridSize(args.extraForbidden, g) : ""}

${commonTextException(g)}

【出力比率】
1:1（正方形）`;
}

